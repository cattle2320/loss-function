import torch
import torch.nn as nn
import numpy as np

from scipy.ndimage import distance_transform_edt


def distance_map_weight(net_out, target):

    posmask = target.astype(np.bool)
    negmask = ~posmask

    pos_edt = distance_transform_edt(posmask)
    pos_edt = (np.max(pos_edt) - pos_edt) * posmask
    neg_edt = distance_transform_edt(negmask)
    neg_edt = (np.max(neg_edt) - neg_edt) * negmask

    res = pos_edt / (np.max(pos_edt) + 1e-9) + neg_edt / (np.max(neg_edt) + 1e-9)
    return torch.from_numpy(res)


def ce_losses(net_out, target, smooth=1e-6):
    denominator = -(target * torch.log(net_out))
    return denominator


def DPCE_loss(net_out, target):
    pixels = net_out.shape[0] * net_out.shape[1]

    distance_map = distance_map_weight(net_out, target.cpu().numpy()) + 1
    if distance_map.device != net_out.device:
        distance_map = distance_map.to(net_out.device).type(torch.float32)
        print(distance_map.device)

    if target.device != net_out.device:
        target = target.to(net_out.device).type(torch.float32)
        print(target.device)

    ce_loss = ce_losses(net_out, target)
    dpce_loss = (distance_map * ce_loss).sum() / pixels
    return dpce_loss


class Mismatch_loss(nn.Module):
    def __init__(self):
        super(Mismatch_loss, self).__init__()

    def forward(self, net_out, target, max_positiones):
        num_class = target.shape[1]
        img_number = target.shape[0]

        img_losses = torch.zeros(img_number)
        for j in range(net_out.shape[0]):

            losses = torch.zeros(num_class)
            for i in range(0, net_out.shape[1]):
                max_target = torch.max(target[j, i, ...])
                max_position = torch.max(max_positiones[j, i, ...])
                if max_target == 0 and max_position == 0:
                    continue
                elif max_target == 1:
                    loss = DPCE_loss(net_out[j, i, ...], target[j, i, ...])
                    losses[i] = loss
            img_losses[j] = losses.sum() / torch.count_nonzero(losses, dim=0)
        return img_losses.sum() / img_number


